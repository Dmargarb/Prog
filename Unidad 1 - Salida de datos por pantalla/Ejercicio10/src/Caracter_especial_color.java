public class Caracter_especial_color {
    public static void main(String[] args) throws Exception {
        System.out.println("\033[0;32m");
        System.out.println("                     **\033[0;34m");
        System.out.println("     Una\033[0;32m             **\033[0;34m");
        System.out.println("    Silla\033[0;32m            **\033[0;34m");
        System.out.println("    Verde\033[0;32m     *********");
        System.out.println("              *********");
        System.out.println("              **     **");
        System.out.println("              **     **");
        System.out.println("              **     **");
        System.out.print ("\033[0m");
        System.out.println ("");
    }
}
